const countFruits = (array) => {
  const occurrences = array.reduce((acc, curr) => {
    return acc[curr] ? ++acc[curr] : acc[curr] = 1, acc
  }, {});

  let result = [];
  
  for (const key in occurrences) {
    let message = `${occurrences[key]} ${key}`;
    if (occurrences[key] > 1) message += 's';
    result.push(message);
  }

  return `Sua cesta possui: ${result.join(', ')}.`
}
